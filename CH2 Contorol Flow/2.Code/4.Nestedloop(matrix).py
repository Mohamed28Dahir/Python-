#Nested Loop

#Tusaale Nested Loop
for i in range(3):  # Loop dibadda
    for j in range(2):  # Loop gudaha
        print(f"i: {i}, j: {j}")
#Sharaxaad
# Loop dibadda wuxuu socdaa 3 jeer (i = 0, 1, 2).
# Loop gudaha wuxuu socdaa 2 jeer (j = 0, 1) markasta oo loop dibadda uu socdo.
# Sidaas darteed, wadarta guud ee iterations waa 3 * 2 = 6.
# Natiijada waxay noqon doontaa:
# i: 0, j: 0
# i: 0, j: 1
# i: 1, j: 0
# i: 1, j: 1
# i: 2, j: 0
# i: 2, j: 1
